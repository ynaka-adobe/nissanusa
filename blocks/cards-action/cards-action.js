export default function decorate(block) {
  const ul = document.createElement('ul');
  [...block.children].forEach((row) => {
    const li = document.createElement('li');
    while (row.firstElementChild) li.append(row.firstElementChild);

    // Get the text label to match an icon
    const text = li.textContent.trim();
    const iconSvg = ICONS[text];

    // Build icon + label structure
    const iconDiv = document.createElement('div');
    iconDiv.className = 'cards-action-card-icon';
    if (iconSvg) {
      iconDiv.innerHTML = iconSvg;
    }

    const bodyDiv = document.createElement('div');
    bodyDiv.className = 'cards-action-card-body';
    // Move remaining content into body
    while (li.firstChild) bodyDiv.append(li.firstChild);

    li.append(iconDiv);
    li.append(bodyDiv);
    ul.append(li);
  });
  block.textContent = '';
  block.append(ul);
}
